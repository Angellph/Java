    private void borrarRegistros() {
        descripcion[lstElementos.getSelectedIndex()] = "";
        for(int i = 0;i<descripcion.length;i++){
            try{
                if(descripcion[i].equals("")&&!descripcion[i + 1].equals("")){
                    descripcion[i] = descripcion[i + 1];
                }
                if(descripcion[i].equals(descripcion[i - 1])){
                descripcion[i] = descripcion[ i + 1];
               
                }
             }catch(Exception e){
                 
             }
        }
        elementos.remove(lstElementos.getSelectedIndex());
    }
